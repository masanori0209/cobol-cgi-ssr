#!/bin/bash
set -eu
cd "$(dirname "$0")/.."
mkdir -p reports

count_file() {
  file="$1"
  if [ -f "$file" ]; then
    wc -l < "$file" | tr -d ' '
  else
    echo 0
  fi
}

sum_glob() {
  total=0
  for f in "$@"; do
    [ -f "$f" ] || continue
    n=$(wc -l < "$f" | tr -d ' ')
    total=$((total + n))
  done
  echo "$total"
}

router=$(count_file src/ssr.cbl)
config=$(count_file src/config.cbl)
template=$(count_file src/ssrtemplate.cbl)
postlist=$(count_file src/postlistfill.cbl)
renderpage=$(count_file src/renderpage.cbl)
cgi=$(sum_glob src/cgilib.cbl src/cgihtmlhdr.cbl src/navbuild.cbl)
posts=$(count_file src/postsdata.cbl)
controllers=$(sum_glob src/controllers/*.cbl)
views=$(find views -name '*.html' -print0 2>/dev/null | xargs -0 wc -l 2>/dev/null | tail -1 | awk '{print $1}')
if [ -z "${views}" ]; then views=0; fi

total=$((router + config + template + postlist + renderpage + cgi + posts + controllers))

cat > reports/line-count.txt <<EOF
# Generated by scripts/count-lines.sh — non-blank lines approximated via wc -l
feature,files,lines
router (PATH_INFO + method dispatch),src/ssr.cbl,${router}
route table,src/config.cbl,${config}
template engine,src/ssrtemplate.cbl,${template}
post list vars,src/postlistfill.cbl,${postlist}
page renderer,src/renderpage.cbl,${renderpage}
CGI POST/cookie/session,cgilib+cgihtmlhdr+navbuild,${cgi}
indexed data (posts.dat),src/postsdata.cbl,${posts}
page controllers,src/controllers/*.cbl,${controllers}
HTML views,views/*.html,${views}
total_cobol,,${total}
total_views,,${views}
EOF

cat reports/line-count.txt
